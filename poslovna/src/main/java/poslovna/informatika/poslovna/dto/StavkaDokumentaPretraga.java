package poslovna.informatika.poslovna.dto;

public class StavkaDokumentaPretraga {
	private float kolicina;
	private float cena;
	private float vrednost;
	private String roba;
	public float getKolicina() {
		return kolicina;
	}
	public void setKolicina(float kolicina) {
		this.kolicina = kolicina;
	}
	public float getCena() {
		return cena;
	}
	public void setCena(float cena) {
		this.cena = cena;
	}
	public float getVrednost() {
		return vrednost;
	}
	public void setVrednost(float vrednost) {
		this.vrednost = vrednost;
	}
	public String getRoba() {
		return roba;
	}
	public void setRoba(String roba) {
		this.roba = roba;
	}
	
}
